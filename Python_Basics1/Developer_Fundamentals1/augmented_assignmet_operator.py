some_value = 5
some_value += 2  # Equivalent to: some_value = some_value + 2
print(some_value)  # Output: 7  

some_value *= 3  # Equivalent to: some_value = some_value * 3
print(some_value)  # Output: 21 
some_value -= 4  # Equivalent to: some_value = some_value - 4
print(some_value)  # Output: 17  
some_value /= 2  # Equivalent to: some_value = some_value / 2
print(some_value)  # Output: 8.5        
some_value %= 3  # Equivalent to: some_value = some_value % 3
print(some_value)  # Output: 2.5
some_value **= 2  # Equivalent to: some_value = some_value ** 2
print(some_value)  # Output: 6.25
some_value //= 2  # Equivalent to: some_value = some_value // 2
print(some_value)  # Output: 3.0
# You can use augmented assignment operators with different data types
text = "Hello"
text += " World!"  # Equivalent to: text = text + " World!"
print(text)  # Output: Hello World!
count = 10
count *= 2  # Equivalent to: count = count * 2
print(count)  # Output: 20
iq = 190
iq /= 2  # Equivalent to: iq = iq / 2
print(iq)  # Output: 95.0
